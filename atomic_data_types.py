"""
Python has atomic data types with some associated operations. Below are a description and an example of each. 
"""
    
# There are two numeric atomic data types - ints and floats. 
# Operators: + (addition), - (subtraction), * (multiplication), / (division), ** (exponentiation), % (modulo), // (integer division)
print(2 + 3) 
print(5 - 2) 
print(4 * 7)
print(9 / 3)
print(2**8)
print(7%3) # Returns the remainder
print(15//8) # Integer division returns the integer portion of the quotient by truncating the fractional part

# Boolean - True & False
# Operators: and, or, not
print(True)
print(False)
print(False or True)
print(not (False or True))
print(True and True)


